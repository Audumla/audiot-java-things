import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

public class TestBean {
    private static final Logger logger = LoggerFactory.getLogger(TestBean.class);
    private String name;
    private int value;
    private int handleCount = 0;

    public TestBean() {
    }

    public TestBean(String name, int value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public void handleMessage(TestBean bean) {
        assert bean.getName().equals(getName());
        assert bean.getValue() == getValue();
        ++handleCount;

    }

    public boolean testMessage(TestBean bean) {
        return bean.getValue() == 1;
    }

    public int getHandleCount() {
        return handleCount;
    }
}
